int		ft_atoi(char *str)
{
	long	n;
	int		minus;

	n = 0;
	minus = 0;
	while (*str == '\n' || *str == '\t' || *str == ' ')
		str++;
	if (*str >= 'A' && *str <= 'z')
		return (0);
	if (*str == '-')
	{
		minus = 1;
		str++;
	}
	else if (*str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		n = n * 10 + *str - '0';
		str++;
	}
	if (minus)
		n = n * (-1);
	return (n);
}
