char	*ft_strrev(char *str)
{
	char	aux;
	int		i;
	int		k;

	i = 0;
	k = 0;
	while (str[k] != '\0')
		k++;
	while (i < k / 2)
	{
		aux = str[i];
		str[i] = str[k - i - 1];
		str[k - i - 1] = aux;
		i++;
	}
	return (str);
}
