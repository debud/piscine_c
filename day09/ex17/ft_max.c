int		ft_max(int *tab, int length)
{
	int maxi;
	int i;

	if (length <= 0)
		return (0);
	maxi = tab[0];
	i = 1;
	while (i < length)
	{
		if (maxi < tab[i])
			maxi = tab[i];
		i++;
	}
	return (maxi);
}
