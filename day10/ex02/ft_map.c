int		*ft_map(int *tab, int length, int (*f)(int))
{
	int	*aux;
	int	i;

	i = 0;
	aux = (int *)malloc(sizeof(int) * (length + 1));
	while (i < length)
	{
		aux[i] = f(tab[i]);
		i++;
	}
	return (aux);
}
