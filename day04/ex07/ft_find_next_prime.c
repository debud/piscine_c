int		ft_find_next_prime(int nb)
{
	int d;
	int ok;

	while (1)
	{
		d = 2;
		ok = 1;
		if (nb <= 1)
			return (2);
		while (d <= nb / 2 && ok)
		{
			if (nb % d == 0)
				ok = 0;
			d++;
		}
		if (ok)
			return (nb);
		nb++;
	}
}
