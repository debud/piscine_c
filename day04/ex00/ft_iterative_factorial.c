int		ft_iterative_factorial(int nb)
{
	int i;
	int p;

	i = 1;
	p = 1;
	if (nb < 0 || nb >= 17)
		return (0);
	else
		while (i <= nb)
		{
			p = p * i;
			i++;
		}
	return (p);
}
